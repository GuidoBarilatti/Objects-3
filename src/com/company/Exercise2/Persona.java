package com.company.Exercise2;

public class Persona {
    private String dni;
    private String name;
    private String lastName;
    private String mail;
    private String direction;

    public Persona(){

    }

    public Persona(String dni, String name, String lastName, String mail, String direction) {
        this.dni = dni;
        this.name = name;
        this.lastName = lastName;
        this.mail = mail;
        this.direction = direction;
    }
}
